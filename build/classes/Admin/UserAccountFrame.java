    /**
     * btnGenerateUserNameActionPerformed generate username button listener
     * @param evt 
     */
    private void btnGenerateUserNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateUserNameActionPerformed
        // get first and last name
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        // check for null values
        if (!firstName.matches("")&&firstName!=null&&!lastName.matches("")&&lastName!=null)
        {
            // Generate new username
            char firstLetter = firstName.charAt(0);
            // combine first leter of first name and last name
            String newUserID = firstLetter + lastName.toLowerCase();
            newUserID = newUserID.toLowerCase();// set to lowercase
            
            //instantiate new QueryAccount object
            QueryAccount qa = new QueryAccount();
            // validate new username
            qa.matchProfile(newUserID, firstName, lastName);
            // get validated userId
            newUserID = qa.getNewUserID();
            txtUserName.setText(newUserID);// display
        }
    }//GEN-LAST:event_btnGenerateUserNameActionPerformed
    // end btnGenerateUserNameActionPerformed method
    
