    public UserAccountFrame(String empID) {
        initComponents();
        this.empID = empID;
        // instantiate new QueryAccount object
        QueryAccount qa = new QueryAccount();
        
        // check fro null id input
        if (empID!=null)// existing account
        {
            btnCreateUser.setVisible(false);
            btnCreateUser.setEnabled(false);
            
            qa.setUserProfile(empID);
            
            String createdDate = qa.getDateCreated();
            
            txtDate.setText(createdDate);
            txtFirstName.setText(qa.getFirstName());
            txtLastName.setText(qa.getLastName());
            txtUserName.setText(empID);
            txtPassword.setText(qa.getPassID());
            
            
            String department = qa.getDepartment();
            String type = qa.getType();
            
            cmbType.setSelectedItem(type);
            cmbDepartment.setSelectedItem(department);
            
            boolean isAdmin = qa.getIsAdmin();
            boolean isManager = qa.getIsManager();
            boolean isEngineer = qa.getIsEngineer();
            boolean isAnalyst = qa.getIsAnalyst();
            boolean isTech = qa.getIsTech();
            
            if (isAdmin==true)
            {
                cbAdmin.doClick();
            }
            if (isManager==true)
            {
                cbManager.doClick();
            }
            if (isEngineer==true)
            {
                cbEngineer.doClick();
            }
            if (isAnalyst==true)
            {
                cbAnalyst.doClick();
            }
            if (isTech==true)
            {
                cbTech.doClick();
            }
        }
        else// new account
        {
            // set button viisibity
            btnDeleteUser.setVisible(false);
            btnUpdateUser.setVisible(false);
            btnDeleteUser.setEnabled(false);
            btnUpdateUser.setEnabled(false);
            // instantiate new Chronology object 
            Chronology c = new Chronology();
            // set date feld with current date then lock text field
            txtDate.setText(c.getDate());
            txtDate.setEditable(false);
            this.defaultPassID = qa.getDefaultPassID();// set default password
        }
        // lock user name
        txtUserName.setEditable(false);
    }// end UserAccountFrame constructor

