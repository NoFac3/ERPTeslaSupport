    /**
     * btnCreateUserActionPerformed create button listener 
     * @param evt 
     */
    private void btnCreateUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateUserActionPerformed
        // get profile attroibutes
        String date = txtDate.getText();
        String userID = txtUserName.getText();
        String passID = txtPassword.getText();
        String type = cmbType.getSelectedItem().toString();
        
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        String name = firstName + " " + lastName;
        String department = cmbDepartment.getSelectedItem().toString();
        boolean isAdmin = false;
        boolean isManager = false;
        boolean isEngineer = false;
        boolean isAnalyst = false;
        boolean isTech = false;
        
        // get profile permissions
        if (cbAdmin.isSelected()) {
            isAdmin = true;
        }
        if (cbManager.isSelected()) {
            isManager = true;
        }
        if (cbEngineer.isSelected()) {
            isEngineer = true;
        }
        if (cbAnalyst.isSelected()) {
            isAnalyst = true;;
        }
        if (cbTech.isSelected()) {
            isTech = true;
        }
            
        QueryAccount qa = new QueryAccount();
        boolean accountCreated = qa.createUserAccount(date, userID, passID, type, name, firstName, lastName, department, isAdmin, isManager, isEngineer, isAnalyst, isTech);
        if (accountCreated==true)
        {
            // Show message
            JOptionPane.showMessageDialog(this, "Account created.", "Successful ", JOptionPane.OK_OPTION);
            //Dispose
            this.dispose();
        }
        else
        {
            // Show message with error, dont dispose
            JOptionPane.showMessageDialog(this, "Account not created.", "Error ", JOptionPane.OK_OPTION);
        }

    }//GEN-LAST:event_btnCreateUserActionPerformed
